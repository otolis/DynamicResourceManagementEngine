import { useState, useEffect } from 'react';
import { Link, useNavigate, useSearchParams } from 'react-router-dom';
import { useAuth } from '../context';
import { apiClient } from '../api/client';
import { CyberButton } from '../components/ui/cyberButton';
import { CyberInput } from '../components/ui/cyberInput';
import { AnimatedCard } from '../components/ui/animatedCard';
import '../styles/pages.css';

export function LoginPage() {
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();
  const { login, isLoading, error } = useAuth();

  const [formData, setFormData] = useState({
    email: '',
    password: '',
    tenantId: searchParams.get('tenant') || apiClient.getTenantId() || 'default-tenant',
  });

  // Sync with URL params if tenant changes
  useEffect(() => {
    const urlTenant = searchParams.get('tenant');
    if (urlTenant) {
      setFormData(prev => ({ ...prev, tenantId: urlTenant }));
    }
  }, [searchParams]);
  const [formError, setFormError] = useState<string | null>(null);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setFormError(null);

    if (!formData.email || !formData.password) {
      setFormError('Email and password are required');
      return;
    }

    try {
      await login(
        { email: formData.email, password: formData.password },
        formData.tenantId
      );
      navigate('/app');
    } catch (err) {
      const message = err instanceof Error ? err.message :
        (err as { message?: string })?.message || 'Login failed';
      setFormError(message);
    }
  };

  return (
    <div className="public-page">
      {/* Navigation */}
      <nav className="public-nav">
        <Link to="/" className="public-nav__logo">
          <span className="public-nav__logo-icon">&#9670;</span>
          DRME
        </Link>
        <div className="public-nav__links">
          <Link to="/" className="public-nav__link">Home</Link>
          <Link to="/features" className="public-nav__link">Features</Link>
          <Link to="/pricing" className="public-nav__link">Pricing</Link>
        </div>
      </nav>

      {/* Login Form */}
      <section className="hero">
        <AnimatedCard>
          <div className="login-card">
            <h2 className="login-card__title">Sign In</h2>

            {(formError || error) && (
              <div className="login-card__error">
                {formError || error}
              </div>
            )}

            <form onSubmit={handleSubmit} className="login-card__form">
              <CyberInput
                label="Organization Slug or ID"
                value={formData.tenantId}
                onChange={(e) => setFormData({ ...formData, tenantId: e.target.value })}
                placeholder="e.g. acme-corp or UUID"
              />

              <CyberInput
                label="Email"
                type="email"
                value={formData.email}
                onChange={(e) => setFormData({ ...formData, email: e.target.value })}
                placeholder="you@example.com"
              />

              <CyberInput
                label="Password"
                type="password"
                value={formData.password}
                onChange={(e) => setFormData({ ...formData, password: e.target.value })}
                placeholder="--------"
              />

              <div className="login-card__forgot">
                <Link to="/forgot-password" className="login-card__forgot-link">
                  Forgot password?
                </Link>
              </div>

              <CyberButton
                type="submit"
                variant="primary"
                size="lg"
                disabled={isLoading}
              >
                {isLoading ? 'Signing in...' : 'Sign In'}
              </CyberButton>

              <div className="login-card__divider">
                <span>OR</span>
              </div>

              <CyberButton
                type="button"
                variant="ghost"
                size="lg"
                onClick={() => window.location.href = `http://127.0.0.1:3000/auth/google?tenant=${formData.tenantId}`}
                className="google-btn"
              >
                <svg className="google-btn__icon" viewBox="0 0 24 24" width="20" height="20">
                  <path fill="currentColor" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                  <path fill="currentColor" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                  <path fill="currentColor" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z" />
                  <path fill="currentColor" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                </svg>
                Sign in with Google
              </CyberButton>
            </form>

            <p className="login-card__footer">
              Don't have an account?{' '}
              <Link to="/register">Create Account</Link>
            </p>
          </div>
        </AnimatedCard>
      </section>
    </div>
  );
}
